public class CheckPoint12_10 {
	/**
	 * 10. How do you add a layout manager to a container?
	 * 
	 * answer: With the setLayout() method adding the type
	 * of layout manager as an argument. [setLayout(new GridLayout());]
	 * 
	 * 11. Which layout manager divides a container into regions known 
	 *       as north, south, east, west, and center? 
	 * 
	 * answer: The BorderLayout manager.
	 * 
	 * 12. Which layout manager arranges components in a row, from 
	 *       left to right, in the order they were added to the container?
	 * 
	 * answer: The FlowLayout manager.
	 * 
	 * 13. Which layout manager arranges components in rows and columns? 
	 * 
	 * answer: The GridLayout manager.
	 * 
	 * 14. How many components can you have at one time in a 
	 *       BorderLayout region? In a GridLayout cell?
	 * 
	 * answer: Only one for both a BorderLayout region and a GridLayout cell.  
	 * 
	 * 15. How do you prevent the BorderLayout manager from resizing a
	 *       component that has been placed in its region?
	 * 
	 * answer: By adding the component to a panel before adding it to 
	 * the BorderLayout region.
	 * 
	 * 16. How can you cause a content pane to be automatically resized
	 *       to accommodate the components contained within it?
	 * 
	 * answer: By adding the pack() method before calling the setVisible()
	 * method.
	 * 
	 * 17. What is the default layout manager for a JFrame object's 
	 *       content pane? For a JPanel object?
	 * 
	 * answer: The default layout manager for a JFrame object's content
	 * pane is the BorderLayout manager. The default layout manager 
	 * for a JPanel object is the FlowLayout manager.
	 */	
}
