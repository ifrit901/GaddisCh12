public class CheckPoint12_18 {
	/**
	 * 18. You want the user to be able to select only one item from a 
	 *        group of items. Which type of component would you use 
	 *        for the items, radio buttons or check boxes?
	 * 
	 * answer: Radio buttons. They are mutually exclusive.
	 * 
	 * 19. You want the user to be able to select any number of items 
	 *       from a group of items. Which type of component would you 
	 *       use for the items, radio buttons or check boxes?
	 * 
	 * answer: Check boxes. More than one can be selected at a time.
	 * 
	 * 20. What is the purpose of a ButtonGroup object? 
	 * 
	 * answer: A ButtonGroup object creates an association between 
	 * the radio buttons so that when one of them is selected, the others
	 * are de-selected.
	 * 
	 * 21. Do you normally add radio buttons, check boxes, or both to 
	 *       a ButtonGroup?
	 * 
	 * answer: Radio buttons.
	 * 
	 * 22. What type of event does a radio button generate when the user
	 *       clicks on it? 	
	 * 
	 * answer: An ActionEvent.
	 * 
	 * 23. What type of event does a check box generate when the user
	 *       clicks on it?
	 * 
	 * answer: An ItemEvent.
	 * 
	 * 24. How do you determine in code whether a radio button is selected?
	 * 
	 * answer: With the isSelected method.
	 * 
	 * 25. How do you determine in code whether a check box is selected?
	 * 
	 * answer: With the is selected method.
	 * 
	 * 26. What method do you use to set a border around a component?
	 * 
	 * answer: The setBorder method.
	 * 
	 * 27. What is the preferred way of creating a Border object?
	 * 
	 * answer: By using the BorderFactory class.
	 */ 		
}
